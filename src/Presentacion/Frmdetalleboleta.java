    private void mtdbuscar() {
        modelo=new DefaultTableModel();
        modelo.addColumn("PRODUCTO");
        modelo.addColumn("CANTIDAD");
        modelo.addColumn("PRECIO VENTA");
        this.tbdetalleboleta.setModel(modelo);
        Clsventa objNC=new Clsventa();
        ClsEDetalleventa objEC=new ClsEDetalleventa();
        objEC.setId(codigo);
        String[] datos=new String[4];
        for(ClsEDetalleventa objE: objNC.Mtdbuscar(objEC))
        {
            datos[0]=objE.getNombrep();
            datos[1]=String.valueOf(objE.getCantidad());
            datos[2]=String.valueOf(objE.getPreventa());
            modelo.addRow(datos);
        }
        this.tbdetalleboleta.setModel(modelo);
    }
