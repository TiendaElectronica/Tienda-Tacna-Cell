    private void btnmodificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnmodificarActionPerformed
        try {
            ClsEEmpleado objEC=new ClsEEmpleado();
            ClsNEmpleado objNC=new ClsNEmpleado();
            int tipousuario =  cboAreaTrb.getSelectedIndex();
            objEC.setId(Integer.parseInt(txtid.getText()));
            objEC.setDni(txtdniM.getText());
            objEC.setCargoempleado(tipousuario);
            objEC.setNombre(txtnombreM.getText());
            objEC.setDireccion(txtdireccionM.getText());
            objEC.setEmail(txtemailM.getText());
            objEC.setTelefono(txttelefonoM.getText());
            File ruta = new File(ruta1);
            try{
                byte[] icono = new byte[(int) ruta.length()];
                InputStream input = new FileInputStream(ruta);
                input.read(icono);
                objEC.setImagen(icono);
            }catch(Exception ex){
                objEC.setImagen(null);
            }
            objEC.setEstado_empleado("ACTIVO");
            objEC.setSueldo(Double.parseDouble(txtsueldoM.getText()));
            int respuesta = JOptionPane.showConfirmDialog(this, "Desea modificar?","Confirmacion"
                ,JOptionPane.YES_NO_OPTION);
            if (respuesta==0)
            {
                if(objNC.MtdModificarempleado(objEC)==true)
                {
                    JOptionPane.showMessageDialog(null, "Datos Guardatos Correctamente");
                    mtdlimpiarcajas();
                }
                else
                {
                    JOptionPane.showMessageDialog(null, "Error datos incompletos");
                }
            }
            else
            {
                mtdlimpiarcajas();
            }
            mtdlistartabla();

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnmodificarActionPerformed

