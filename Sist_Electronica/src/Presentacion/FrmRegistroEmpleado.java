    private void btnregistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnregistrarActionPerformed
        ClsEEmpleado objEC=new ClsEEmpleado();
        ClsNEmpleado objNC=new ClsNEmpleado();
        Date date=new Date();
        DateFormat fechaHora=new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
        int tipousuario =  cbocargo.getSelectedIndex();
        if(!txtdni.getText().equals("")&& !txtdireccion4.equals("") && !txtnombre.getText().equals("")&& !txtemail1.equals("") && !txttelefono.getText().equals("") && !txtsueldo.getText().equals(""))
        {
            objEC.setDni(txtdni.getText());
            objEC.setCargoempleado(tipousuario);
            objEC.setNombre(txtnombre.getText());
            objEC.setDireccion(txtdireccion4.getText());
            objEC.setEmail(txtemail1.getText());
            objEC.setTelefono(txttelefono.getText());
            if(rbtnfemenino.isSelected()==true)
            {
                objEC.setSexo("Femenino");
            }
            else if(rbtnmasculino.isSelected()==true)
            {
                objEC.setSexo("Masculino");
            }
            objEC.setFecha(fechaHora.format(date));
            File ruta = new File(ruta1);
            try{
                byte[] icono = new byte[(int) ruta.length()];
                InputStream input = new FileInputStream(ruta);
                input.read(icono);
                objEC.setImagen(icono);
            }catch(Exception ex){
                objEC.setImagen(null);
            }
            objEC.setEstado_empleado("ACTIVO");
            objEC.setSueldo(Double.parseDouble(txtsueldo.getText()));
            //objEC.setEstado(1);
            if(objNC.VerificarExiste(objEC)==true)
            {
                JOptionPane.showMessageDialog(null, "El Nro de DNI ya existe");
            }
            else
            {
                objNC.MtdRegistrarEmpleado(objEC);
                mtdlimpiarcajas();

            }
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Existen campos vacios");
        }
    }//GEN-LAST:event_btnregistrarActionPerformed

