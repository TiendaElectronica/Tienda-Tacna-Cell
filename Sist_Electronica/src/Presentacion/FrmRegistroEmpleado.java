    private void mtdlistartabla() {
        mtdindicestabla();
        ClsNEmpleado objNC=new ClsNEmpleado();
        String[] datos=new String[11];
        for(ClsEEmpleado objE: objNC.MtdListar())
        {
            datos[0]=String.valueOf(objE.getId());
            datos[1]=objE.getDni();
            datos[2]=objE.getCargo_empleadonombre();
            datos[3]=objE.getNombre();
            datos[4]=objE.getDireccion();
            datos[5]=objE.getEmail();
            datos[6]=objE.getTelefono();
            datos[7]=objE.getSexo();
            datos[8]=objE.getEstado_empleado();
            datos[9]=String.valueOf(objE.getSueldo());
            datos[10]=objE.getFecha();
            modelo.addRow(datos);

        }
        this.tblRegistrosEmpl.setModel(modelo);
        
        
    }

