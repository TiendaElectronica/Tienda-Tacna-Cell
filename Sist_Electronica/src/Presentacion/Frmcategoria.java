    private void btnregistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnregistrarActionPerformed
        ClsECategoriaP objEC=new ClsECategoriaP();
        ClsNCategoriaP objNC=new ClsNCategoriaP();
        Date date=new Date();
        DateFormat fechaHora=new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
        objEC.setCategoria(txtcategoria.getText());
        objEC.setFecha(fechaHora.format(date));
        if(objNC.mtdguardarcategoria(objEC)==true)
        {
            JOptionPane.showMessageDialog(null, "Datos Guardatos correctamente");
            mtdlistar();
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Error al ingresar los datos");
        }
        

    }//GEN-LAST:event_btnregistrarActionPerformed

