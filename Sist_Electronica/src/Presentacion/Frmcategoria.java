    private void mtdlistar() {
        modelo=new DefaultTableModel();
        modelo.addColumn("ID");
        modelo.addColumn("CATEGORIA");
        modelo.addColumn("FECHA");
        this.tbcategoriap.setModel(modelo);
        ClsNCategoriaP objNC=new ClsNCategoriaP();
        String[] datos=new String[3];
        for(ClsECategoriaP objE: objNC.MtdListar())
        {
            datos[0]=String.valueOf(objE.getId());
            datos[1]=objE.getCategoria();
            datos[2]=objE.getFecha();
            modelo.addRow(datos);
        }
        this.tbcategoriap.setModel(modelo);
    }
